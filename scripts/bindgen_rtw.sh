# Regenerate allowlisted Rust bindings for the crypto pilot (W1-01).
#
# Usage (from repo root):
#   ./scripts/bindgen_rtw.sh
#   ./scripts/bindgen_rtw.sh --check          # fail if committed blob is stale
#   CHECK=1 ./scripts/bindgen_rtw.sh          # same as --check
#   KDIR=/path/to/rust-enabled-kernel ./scripts/bindgen_rtw.sh
#
# Environment:
#   KDIR            Pinned Rust-enabled kernel tree (default: /opt/linux).
#                   Wave 0 contract pin: must exist so regenerate stays tied to
#                   the same kernel used for L0. Not passed to clang yet — the
#                   aes.h pilot surface only needs freestanding C types; future
#                   waves may add kernel include paths here.
#   BINDGEN         bindgen binary (default: bindgen on PATH).
#   LIBCLANG_PATH   Optional; set if bindgen cannot find libclang.
#   CHECK           If set to 1, regenerate without writing (see --check).
#
# Requires bindgen-cli 0.65.x on PATH (kernel-pinned; see
# docs/rust-migration/dev-environment.md).
#
# Output: rust/bindings/bindings_generated.rs (committed; re-run after changing
# the helper header or allowlists below).

set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
HELPER="${ROOT}/rust/bindings/bindgen_helper.h"
OUT="${ROOT}/rust/bindings/bindings_generated.rs"
KDIR="${KDIR:-/opt/linux}"
BINDGEN="${BINDGEN:-bindgen}"

CHECK_MODE=0
if [[ "${CHECK:-0}" == "1" ]]; then
	CHECK_MODE=1
fi
for arg in "$@"; do
	case "${arg}" in
	--check) CHECK_MODE=1 ;;
	-h | --help)
		sed -n '2,30p' "$0"
		exit 0
		;;
	*)
		echo "error: unknown argument: ${arg}" >&2
		echo "usage: $0 [--check]" >&2
		exit 1
		;;
	esac
done

if ! command -v "${BINDGEN}" >/dev/null 2>&1; then
	echo "error: bindgen not found on PATH (install bindgen-cli 0.65.1 --locked)" >&2
	exit 1
fi

if [[ ! -f "${HELPER}" ]]; then
	echo "error: missing helper header: ${HELPER}" >&2
	exit 1
fi

# Wave 0 pinned-kernel contract (existence only for this pilot surface).
if [[ ! -d "${KDIR}" ]]; then
	echo "error: KDIR not a directory: ${KDIR}" >&2
	echo "hint: pin a Rust-enabled kernel per docs/rust-migration/dev-environment.md" >&2
	exit 1
fi

TMP_OUT="$(mktemp)"
TMP_BANNERED="$(mktemp)"
trap 'rm -f "${TMP_OUT}" "${TMP_BANNERED}"' EXIT

# Match kernel rust/Makefile bindgen baseline where practical.
# Allowlist: AES encrypt API + AES_BLOCK_SIZE only (pilot callers of aes-ctr).
# Decrypt symbols in aes.h stay excluded until a later chunk needs them.
"${BINDGEN}" \
	"${HELPER}" \
	--rust-target 1.68 \
	--use-core \
	--ctypes-prefix core::ffi \
	--no-layout-tests \
	--no-doc-comments \
	--allowlist-function '^aes_encrypt$' \
	--allowlist-function '^aes_encrypt_init$' \
	--allowlist-function '^aes_encrypt_deinit$' \
	--allowlist-var '^AES_BLOCK_SIZE$' \
	-o "${TMP_OUT}" \
	-- \
	-I"${ROOT}/core/crypto" \
	-D__BINDGEN__

{
	cat <<'EOF'
/* automatically generated by scripts/bindgen_rtw.sh — do not edit by hand */
/* W1-01 allowlisted bindings: AES encrypt API for the aes-ctr pilot */

EOF
	if head -n1 "${TMP_OUT}" | grep -q 'automatically generated by rust-bindgen'; then
		tail -n +2 "${TMP_OUT}"
	else
		cat "${TMP_OUT}"
	fi
} >"${TMP_BANNERED}"

if [[ "${CHECK_MODE}" -eq 1 ]]; then
	if [[ ! -f "${OUT}" ]]; then
		echo "error: missing committed bindings: ${OUT}" >&2
		exit 1
	fi
	if ! diff -u "${OUT}" "${TMP_BANNERED}"; then
		echo "error: ${OUT} is stale; run ./scripts/bindgen_rtw.sh and commit the result" >&2
		exit 1
	fi
	echo "OK: ${OUT} matches bindgen output"
	exit 0
fi

mv "${TMP_BANNERED}" "${OUT}"
trap 'rm -f "${TMP_OUT}"' EXIT
echo "Wrote ${OUT}"
